def split_urls(urls, file_base_name="urls", urls_per_file=99):
    # Calculate the total number of files
    num_files = len(urls) // urls_per_file + (len(urls) % urls_per_file > 0)

    # Split the URLs into chunks
    for i in range(num_files):
        start = i * urls_per_file
        end = start + urls_per_file
        # Create a new file for each chunk
        with open(f"{file_base_name}_{i+1}.txt", "w") as file:
            # Write the URLs to the file
            for url in urls[start:end]:
                file.write(url + "\n")
        print(f"Created file: {file_base_name}_{i+1}.txt")

